#include <iostream>
#include "mathFunctions.h"
using namespace std;

// Function to add two numbers
int add(int a, int b)
{
    return a + b;
}

// Function to subtract two numbers
int subtract(int a, int b)
{
    return a - b;
}

// Function to display result
void displayResult(int result)
{
    cout << "Result: " << result << endl;
}